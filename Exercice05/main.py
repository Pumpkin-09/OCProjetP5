def sum(a, b):
    """
    Retourne la somme de deux nombres, soit deux int, soit deux float.
    
    Args:
    a: le premier nombre.
    b: le deuxieme nombre.
    
    Returns:
    la somme de a et b.
    """
    return a + b
 
def subtraction(a, b):
    """
    Retourne la difference de deux nombres, soit deux int, soit deux float.
    
    Args:
    a: le premier nombre.
    b: le deuxieme nombre.
    
    Returns:
    la difference de a et b.
    """
    return a - b
